using Discord;

namespace Template.Appearance;

/// <summary>
/// Represents a constant set of predefined <see cref="Emote"/> values.
/// </summary>
public static class Emotes
{
    /// <summary>
    /// The emote used to destroy an embed.
    /// </summary>
    public static readonly Emote Exit = "<:exit:1085528150211035186>";

    /// <summary>
    /// The emote used for navigating forward.
    /// </summary>
    public static readonly Emote Forward = "<:forward:1083786970569912321>";

    /// <summary>
    /// The emote used for navigating backward.
    /// </summary>
    public static readonly Emote Backward = "<:backward:1083786986608939148>";
}
